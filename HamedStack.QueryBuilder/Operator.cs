namespace HamedStack.QueryBuilder;

/// <summary>
/// Represents logical operators for building query expressions.
/// </summary>
public enum Operator
{
    /// <summary>
    /// Represents the "And" logical operator.
    /// </summary>
    And,

    /// <summary>
    /// Represents the "Or" logical operator.
    /// </summary>
    Or,

    /// <summary>
    /// Represents the "Xor" logical operator.
    /// </summary>
    Xor
}